//Assignment operator in java 

package Basic;


public class C7_AssignmentOperator
{
        public static void main(String[] args)
            {
		         //  Assignment operator:  +=,-+,*=,/=,%=	
        	     int No=90;
        	     System.out.println(No==100);
        	     System.out.println(No+=50);
        	     System.out.println(No-=50);
        	     System.out.println(No*=50);
        	     System.out.println(No/=100);
        	     System.out.println(No%=50);
	    	}
}
