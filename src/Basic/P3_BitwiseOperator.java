//Bitwise oprator in java


package Basic;

public class P3_BitwiseOperator {
      public static void main(String[] args) {
    	  //Bitwise operator-- &,|,^,>>,<<
		int no1=32;
		int no2=12;
		System.out.println(no1&no2);
		System.out.println(no1|no2);
		System.out.println(no1^no2);
		System.out.println(no1>>no2);
		System.out.println(no1<<no2);
		
	}
}
