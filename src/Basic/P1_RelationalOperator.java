//Relational operator in java

package Basic;

public class P1_RelationalOperator
{
       public static void main(String[] args)
       
       {
		//Relational Operator:   <,>,>=,<=,!=,==
    	   int a=5;
    	   int b=6;
    	   System.out.println(a>b);
    	   System.out.println(a<b);
    	   System.out.println(a>=b);
    	   System.out.println(a<=b);
    	   System.out.println(a!=b);
	  }
}
