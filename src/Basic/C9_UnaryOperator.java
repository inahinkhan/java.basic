//Unary operator in java

package Basic;

public class C9_UnaryOperator
{
       public static void main(String[] args)
        {
	           //Unary operator--	   +,-,,++,--
    	   int No1=10;
    	   int Result=-No1;
    	   System.out.println(Result);
    	   int No2=-25;
    	   int Ans=-No2;
    	   System.out.println(Ans);
    	   
    	   System.out.println(++No1);
    	   System.out.println(--No1);

	   }
}
